// Función helper para verificar si una ruta está activa según el horario actual
export function isRouteActive(route) {
  const now = new Date();
  const dayNames = ["Domingo", "Lunes", "Martes", "Miércoles", "Jueves", "Viernes", "Sábado"];
  const currentDay = dayNames[now.getDay()];
  const currentTime = `${now.getHours().toString().padStart(2, '0')}:${now.getMinutes().toString().padStart(2, '0')}`;
  
  const daySchedule = route.schedule[currentDay];
  if (!daySchedule || daySchedule.length === 0) return false;
  
  // Verificar si la hora actual está dentro de una ventana de 2 horas después del horario programado
  return daySchedule.some(scheduledTime => {
    const [scheduledHour, scheduledMin] = scheduledTime.split(':').map(Number);
    const [currentHour, currentMin] = currentTime.split(':').map(Number);
    
    const scheduledMinutes = scheduledHour * 60 + scheduledMin;
    const currentMinutes = currentHour * 60 + currentMin;
    
    // La ruta está activa si estamos dentro de 2 horas después del horario programado
    return currentMinutes >= scheduledMinutes && currentMinutes <= scheduledMinutes + 120;
  });
}

// Función helper para obtener el punto más cercano en una ruta a una posición dada
export function getClosestPointOnRoute(route, position) {
  let minDistance = Infinity;
  let closestPoint = route.recorrido[0];
  
  route.recorrido.forEach(point => {
    const distance = Math.sqrt(
      Math.pow(point[0] - position.lat, 2) + 
      Math.pow(point[1] - position.lng, 2)
    );
    if (distance < minDistance) {
      minDistance = distance;
      closestPoint = point;
    }
  });
  
  return closestPoint;
}

